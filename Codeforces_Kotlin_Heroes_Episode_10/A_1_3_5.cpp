#include <bits/stdc++.h>

using namespace std;

int main() {

    int tt;
    cin >> tt;
    while (tt--) {
        int n;
        cin >> n;
        if (n == 2) {
            cout << "2\n"; 
        }
        else if (n == 7) {
            cout << "1\n";
        }
        else {
            cout << "0\n";
        }
    }
}